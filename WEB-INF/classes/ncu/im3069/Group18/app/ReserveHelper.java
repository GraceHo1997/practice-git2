    public JSONObject create(Reserve reserve) {
        /** 記錄實際執行之SQL指令 */
        String exexcute_sql = "";
        long id = -1;
        JSONArray osa = new JSONArray();
        
        try {
            /** 取得資料庫之連線 */
            conn = DBMgr.getConnection();
            /** SQL指令 */
            String sql = "INSERT INTO `missa`.`reserve`(`date`, `note`, `complete_status`, `name`, `email`, `phone`, `rating`)"
                    + " VALUES(?, ?, ?, ?, ?, ?, ?)";
            
            /** 取得所需之參數 */
            String date = reserve.getDate();
            String note = reserve.getNote();
            String complete_status = reserve.getCompleteStatus();
            String name = reserve.getName();
            String email = reserve.getEmail();
            String phone = reserve.getPhone();
            String rating = reserve.getRating();
            
            /** 將參數回填至SQL指令當中 */
            pres = conn.prepareStatement(sql, Statement.RETURN_GENERATED_KEYS);
            pres.setString(1, date);
            pres.setString(2, note);
            pres.setString(3, complete_status);
            pres.setString(4, name);
            pres.setString(5, email);
            pres.setString(6, phone);
            pres.setString(7, rating);
            
            /** 執行新增之SQL指令並記錄影響之行數 */
            pres.executeUpdate();
            
            /** 紀錄真實執行的SQL指令，並印出 **/
            exexcute_sql = pres.toString();
            System.out.println(exexcute_sql);
            
            ResultSet rs = pres.getGeneratedKeys();

            if (rs.next()) {
                id = rs.getLong(1);
                ArrayList<ReserveItem> opd = reserve.getReserveService();
                osa = osh.createByList(id, opd);
            }
        } catch (SQLException e) {
            /** 印出JDBC SQL指令錯誤 **/
            System.err.format("SQL State: %s\n%s\n%s", e.getErrorCode(), e.getSQLState(), e.getMessage());
        } catch (Exception e) {
            /** 若錯誤則印出錯誤訊息 */
            e.printStackTrace();
        } finally {
            /** 關閉連線並釋放所有資料庫相關之資源 **/
            DBMgr.close(pres, conn);
        }

        /** 將SQL指令、花費時間與影響行數，封裝成JSONObject回傳 */
        JSONObject response = new JSONObject();
        response.put("reserve_id", id);
        response.put("reserve_service_id", osa);

        return response;
    }
    
